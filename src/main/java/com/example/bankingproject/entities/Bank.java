package com.example.bankingproject.entities;

import org.apache.ibatis.type.Alias;

@Alias("Bank")
public class Bank {
	private int id;
	private String name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
